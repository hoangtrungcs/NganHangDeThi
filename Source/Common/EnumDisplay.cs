namespace Common {
    public class EnumDisplay {
        public EnumDisplay(string text, int value) {
            DisplayText = text;
            Value = value;
        }
        public EnumDisplay(string text, int value,object tag) {
            DisplayText = text;
            Value = value;
            Tag = tag;
        }
        public string DisplayText { get; set; }
        public int Value { get; set; }
        public object Tag { get; set; }
    }
}
